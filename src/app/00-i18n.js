const I18N={en:EN};
/* t(s): traduce una stringa d'interfaccia (chiave = italiano). */
function t(s){ if(LANG==='it'||s==null) return s; const d=I18N[LANG]; const v=d&&d[String(s)]; return v==null?s:v; }

/* Nomi inglesi degli esercizi (free-exercise-db) per NOME ITALIANO — POPOLATO da
   app/00b-esercizi-en.js (Object.assign su ESEN), da tools/esercizi-en.json. La mappa è
   diretta nome-IT → nome-EN (nessuna dipendenza dal catalogo caricato). Vuoto = fallback IT. */
let ESEN={};
/* exName(nome): nome dell'esercizio da mostrare a schermo. In EN usa il nome inglese
   originale; se manca (es. esercizi CUSTOM o rinominati) resta l'italiano.
   Il nome ITALIANO resta la CHIAVE dei dati salvati: non si tocca mai. */
function exName(nome){
  if(LANG==='it'||!nome) return nome;
  return ESEN[nome]||nome;
}

/* Cambia lingua e ricarica (i dati sono già salvati). */
function setLang(l){
  if(l!=='it'&&l!=='en') return;
  try{ localStorage.setItem('tms-lang',l); }catch(e){}
  LANG=l;
  try{ location.reload(); }catch(e){/* jsdom: nessun reload */}
}

/* Traduce il markup STATICO (pagina/03-corpo.html), marcato con:
     data-i18n        → textContent
     data-i18n-html   → innerHTML (per testi con <strong>/<br>)
     data-i18n-title  → attributo title
     data-i18n-ph     → attributo placeholder
   La sorgente italiana viene memorizzata al primo passaggio (dataset.*Src) così la
   funzione è idempotente anche senza reload. */
function translateStatic(root){
  const R=root||document;
  R.querySelectorAll('[data-i18n]').forEach(el=>{
    if(el.dataset.i18nSrc==null) el.dataset.i18nSrc=el.textContent.trim();
    el.textContent=t(el.dataset.i18nSrc);
  });
  R.querySelectorAll('[data-i18n-html]').forEach(el=>{
    if(el.dataset.i18nHtmlSrc==null) el.dataset.i18nHtmlSrc=el.innerHTML.trim();
    el.innerHTML=t(el.dataset.i18nHtmlSrc);
  });
  R.querySelectorAll('[data-i18n-title]').forEach(el=>{
    if(el.dataset.i18nTitleSrc==null) el.dataset.i18nTitleSrc=(el.getAttribute('title')||'').trim();
    el.setAttribute('title',t(el.dataset.i18nTitleSrc));
  });
  R.querySelectorAll('[data-i18n-ph]').forEach(el=>{
    if(el.dataset.i18nPhSrc==null) el.dataset.i18nPhSrc=(el.getAttribute('placeholder')||'').trim();
    el.setAttribute('placeholder',t(el.dataset.i18nPhSrc));
  });
}
